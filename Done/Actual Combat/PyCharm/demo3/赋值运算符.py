num1 = 10
num2 = 20
f1 = 3.14
str1 = "hello"

num1, num2, f1, str1 = 10, 20, 3.14, "hello" # 批量赋值操作


# 复合赋值运算符
num2 += num1 # num2 = num2 + num1

num2 -= num1 # num2 = num2 - num1
num2 *= num1 # num2 = num2 * num1
num2 /= num1 # num2 = num2 / num1
num2 //= num1 # num2 = num2 // num1
num2 %= num1 # num2 = num2 % num1
num2 **= num1 # num2 = num2 ** num1